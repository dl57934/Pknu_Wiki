package DTO;

public class webInfoDTO {
    int writingPage;
    int member;
    int discussionNum;

    public int getWritingPage() {
        return writingPage;
    }

    public void setWritingPage(int writingPage) {
        this.writingPage = writingPage;
    }

    public int getMember() {
        return member;
    }

    public void setMember(int member) {
        this.member = member;
    }

    public int getDiscussionNum() {
        return discussionNum;
    }

    public void setDiscussionNum(int discussionNum) {
        this.discussionNum = discussionNum;
    }
}
